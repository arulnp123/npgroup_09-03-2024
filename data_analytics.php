			<h3>Understanding Data Analytics</h3>
          <p>Data analytics is a broad term that encompasses many diverse types of data analysis. Any type of information can be subjected to data analytics techniques to get insight that can be used to improve things. Data analytics techniques can reveal trends and metrics that would otherwise be lost in the mass of information. 
		  This information can then be used to optimize processes to increase the overall efficiency of a business or system.</p>
		  <p>For example, manufacturing companies often record the runtime, downtime, and work queue for various machines and then analyze the data to better plan the 
		  workloads so the machines operate closer to peak capacity.</p>
		  <p>Data analytics can do much more than point out bottlenecks in production. Gaming companies use data 
		  analytics to set reward schedules for players that keep the majority of players active in the game. Content companies use many of the same data analytics 
		  to keep you clicking, watching, or re-organizing content to get another view or another click.</p>
		  <p>Data analytics can do much more than point out bottlenecks in production. Gaming companies use data 
