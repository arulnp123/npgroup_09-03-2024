		  to keep you clicking, watching, or re-organizing content to get another view or another click.</p>
		  <P>Data analytics is important because it helps businesses optimize their performances. Implementing it into the business model means companies can help reduce costs by identifying more efficient ways of doing business and by storing large amounts of data. A company can also use data analytics to
		  make better business decisions and help analyze customer trends and satisfaction, which can lead to new—and better—products and services<P/>
		  <h3>Data Analysis Steps</h3>
		  <h4>The process involved in data analysis involves several different steps:</h4>
		  <P>1.	The first step is to determine the data requirements or how the data is grouped. Data may be separated by age, 
		  demographic, income, or gender. Data values may be numerical or be divided by category.</P>
		  <P>2.	The second step in data analytics is the process of collecting it. This can be done through a variety of sources such as computers, 
		  online sources, cameras, environmental sources, or through personnel.</P>
		  <P>3.	Once the data is collected, it must be organized so it can be analyzed. This may take place on a spreadsheet or 
		  other form of software that can take statistical data.</P>
		  <P>4.	The data is then cleaned up before analysis. This means it is scrubbed and checked to ensure there is no duplication or error, and that it is not incomplete. This step helps correct any errors before
		  it goes on to a data analyst to be analyzed.</P>
		  
		  <h3>Types of Data Analytics</h3>
		  <h4>Data analytics is broken down into four basic types:</h4>
		  <P>1.	Descriptive analytics: This describes what has happened over a given period of time. Have the 
		  number of views gone up? Are sales stronger this month than last?</P>
		  <P>2.	Diagnostic analytics: This focuses more on why something happened. This involves more diverse data inputs and a bit of
		  hypothesizing. Did the weather affect beer sales? Did that latest marketing campaign impact sales?</P>
		  <P>3.	Predictive analytics: This moves to what is likely going to happen in the near term. What happened to sales
		  the last time we had a hot summer? How many weather models predict a hot summer this year?</P>
		  <P>4.	Prescriptive analytics: This suggests a course of action. If the likelihood of a hot summer is 
		  measured as an average of these five weather models is above 58%, 
		  we should add an evening shift to the brewery and rent an additional tank to increase output.</P>
		  
		  
